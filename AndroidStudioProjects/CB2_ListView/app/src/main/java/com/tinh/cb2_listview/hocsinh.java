package com.tinh.cb2_listview;

public class hocsinh {
    String ten;
    int tuoi;
    int anh;

    public hocsinh(String ten, int tuoi, int anh) {
        this.ten = ten;
        this.tuoi = tuoi;
        this.anh = anh;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public int getTuoi() {
        return tuoi;
    }

    public void setTuoi(int tuoi) {
        this.tuoi = tuoi;
    }

    public int getAnh() {
        return anh;
    }

    public void setAnh(int anh) {
        this.anh = anh;
    }
}
