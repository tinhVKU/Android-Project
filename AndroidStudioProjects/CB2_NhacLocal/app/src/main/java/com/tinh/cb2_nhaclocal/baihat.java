package com.tinh.cb2_nhaclocal;

public class baihat {
    String ten;
    int diachi;
    public baihat(String ten, int diachi) {
        this.ten = ten;
        this.diachi = diachi;
    }
    public String getTen() {
        return ten;
    }
    public void setTen(String ten) {
        this.ten = ten;
    }
    public int getDiachi() {
        return diachi;
    }
    public void setDiachi(int diachi) {
        this.diachi = diachi;
    }
}
