package com.tinh.cb2_vongquay;

public class vongquay {
    int goc;
    String phanthuong;

    public vongquay(int goc, String phanthuong) {
        this.goc = goc;
        this.phanthuong = phanthuong;
    }

    public int getGoc() {
        return goc;
    }

    public void setGoc(int goc) {
        this.goc = goc;
    }

    public String getPhanthuong() {
        return phanthuong;
    }

    public void setPhanthuong(String phanthuong) {
        this.phanthuong = phanthuong;
    }
}
